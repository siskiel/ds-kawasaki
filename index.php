                <h2 class="mb-4">Selama 1918</h2>
                <p>Aircraft Department didirikan di Hyogo Works hanya 15 tahun setelah penerbangan Wright brothers' virgin. Departemen tersebut mulai memproduksi pesawat pada saat pesawat terbang, yang hanya bisa terbang beberapa jam, masih terbuat dari kain dan kayu. </p>
                <p>Tidak lama setelah itu, sebuah pabrik pembuat pesawat terbang dibangun, dan pesawat pertama Jepang yang terbuat dari logam dikerjakan di pabrik ini.</p>
                <p>
