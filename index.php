                <h2 class="mb-4">Tahun Berikutnya</h2>
                <p>Sebuah awal dari banyaknya perubahan, Marine Freight Departement dipisahkan dan dibentuk menjadi Kawasaki Kisen Kaisha Ltd. (K-Line). Pada tahun 1928, Hyogo Works dipecah menjadi Kawasaki Rolling Stock Manufacturing Co, Ltd. </p>
                <p>Aircraft Department berubah menjadi Kawasaki Aircraft Co, Ltd pada tahun 1937, dan pada tahun 1950 Steel Making Department diubah menjadi Kawasaki Steel Corporation. Departemen Rolling Stock, Aircraft, Marine Freight dan Steel Making menjadi perusahaan dengan haknya sendiri, membangun landasan yang kuat di bidangnya masing-masing.</p>
                <p>
