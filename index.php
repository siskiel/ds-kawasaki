                <h2 class="mb-4">Tahun 1878, Shozo Kawasaki</h2>
                <p>Pendiri Kawasaki, membuka shipyard untuk membangun kapal laut baja. Pada tahun 1896, Kawasaki Dockyard didirikan dan Kojiro Matsukata diangkat sebagai presiden perusahaan pertama. </p>
                <p>Pembuatan lokomotif, gerbong barang, gerbong penumpang, dan girder jembatan dimulai pada tahun 1906 di Hyogo Works yang baru dibuka, dan pada tahun berikutnya, produksi turbin uap laut dimulai di dokyard.</p>
                <p>
