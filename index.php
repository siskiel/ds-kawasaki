                <p>Sepeda motor merk Kawasaki pertama kali diproduksi oleh Kawasaki Aircraft Co., Ltd.</p>
                <p>Kemudian pada tahun 1969 Kawasaki Dockyard, Kawasaki Rolling Stock Manufacturing, dan Kawasaki Aircraft bergabung menjadi satu perusahaan raksasa, yaitu Kawasaki Heavy Industries, Ltd., untuk lebih bersaing di negara berkembang dan pasar global yang sangat kompetitif.</p>
                <p>
              </div>

              <div class="tab-pane fade py-5" id="v-pills-4" role="tabpanel" aria-labelledby="v-pills-4-tab">
