                <h2 class="mb-4">Ringkasan</h2>
                <p>Mewakili perusahaan teknologi yang kegiatannya berkisar dari proyek besar berskala internasional hingga barang-barang yang digunakan dalam kehidupan sehari-hari, hingga untuk rekreasi. Dan di setiap langkahnya, Kawasaki selalu memberikan perhatian penuh kepada manusia dan lingkungan. </p>
                <p>120 tahun berinovasi telah membuat Kawasaki membangun fondasi yang kuat sebagai perusahaan teknologi terkemuka. Sekarang, perusahaan ini sepenuhnya siap menyambut abad baru dan berharap dapat memainkan peran utama dalam kemajuan umat manusia untuk abad-abad seterusnya yang penuh inovasi.</p>
