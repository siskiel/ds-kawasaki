                <h2 class="mb-4">Saat Ini</h2>
                <p>Kawasaki Heavy Industries, Ltd. (KHI) terlibat dalam pembangunan sistem transportasi abad 21 dan, dalam pelaksanaannya, memanfaatkan kekayaan pengetahuan teknologi yang telah terkumpul selama 120 tahun terakhir.</p>
                <p>Ship Building Division telah memimpin dunia dalam memproduksi kapal yang semakin besar, semakin cepat, dan semakin canggih dengan sistem automated ship. KHI terus berupaya menemukan cara untuk meningkatkan efisiensi manufaktur dan efisiensi navigasi namun tetap menghemat energi. Sejauh ini, hal tersebut telah menghasilkan pengembangan Liquid Natural Gas (LNG), kapal high-speed, dan teknologi kelautan canggih lainnya.</p>
                <p>
              </div>

